# Obter o ano de nascimento do usuário
ano_nascimento = int(input("Digite o ano de nascimento: "))

# Obter o ano atual (assumindo que este seja o ano atual)
ano_atual = 2024  # Pode ser substituído pelo ano atual real

# Calcular a idade atual
idade_atual = ano_atual - ano_nascimento

# Exibir a idade atual
print(f"Sua idade atual é: {idade_atual} anos")